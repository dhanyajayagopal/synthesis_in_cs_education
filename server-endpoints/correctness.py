# Suite testing

def nodes_equal(n1, n2):
    if n1 is None and n2 is None:
        return True
    if n1 is None or n2 is None:
        return False
    return n1.val == n2.val \
            and nodes_equal(n1.left, n2.left) \
            and nodes_equal(n1.right, n2.right)

def equal(a, b):
    if a is None and b is None:
        return True
    elif True and isinstance(a, Node) and isinstance(b, Node):
        return nodes_equal(a, b)
    else:
        return a == b

def check(f1, f2, only_out):
    suite1 = make_suite(only_out)
    suite2 = make_suite(only_out)
    for i in range(len(suite1)):
        entry1 = suite1[i]
        entry2 = suite2[i]
        try:
            if not equal(f1(*entry1), f2(*entry2)):
                return False
        except:
            return False
    return True
